using UnityGLTF.KhrCharacter;
using Samples.Shared;

namespace KhrCharacterTestbed.Tests
{
    /// <summary>
    /// Runtime assertions for the animation pipeline shipped in commit 453d5e7
    /// (HumanoidClipFactory + AnimationClipCatalog + AnimationBinder + three demo scenes).
    ///
    /// SceneSmokeTests already covers "does the scene boot" for the three animation demos, but
    /// that suite would happily pass a runtime binding regression that leaves the character
    /// stationary (bind returns null, no Avatar assigned, Playables graph missing) as long as
    /// the scene finishes booting without a raised exception. This suite closes that gap: it
    /// asserts each of the pipeline's contract points against SC-Body (the canonical humanoid-
    /// capable synthetic fixture, generated by SampleCharacterFactory.GenerateSCBody).
    ///
    /// Anti-hollow: every assertion goes through the real plugin types (KhrCharacter,
    /// SkeletonMap) and the real pipeline entrypoints (AnimationBinder.Bind / Play), not mocks.
    /// A regression in KhrCharacter.SkeletonMap.BuildHumanoidAvatar would fail here.
    /// </summary>
    public class SandboxAnimationBinderTests
    {
        private readonly List<Object> _created = new List<Object>();

        [TearDown]
        public void TearDown()
        {
            foreach (var o in _created)
                if (o != null) Object.DestroyImmediate(o);
            _created.Clear();
        }

        // ── Humanoid mode: SC-Body has a full skeleton mapping, so Humanoid must build ─────────

        [UnityTest]
        public IEnumerator Bind_Humanoid_OnSCBody_SucceedsAndAssignsAvatar()
        {
            var load = SandboxTestUtil.LoadSynthetic("SC-Body.glb", _created);
            yield return load;
            var character = load.Current;

            Assert.IsTrue(AnimationBinder.IsSupported(character, RigMode.Humanoid),
                "SC-Body has a KHR skeleton_mapping; IsSupported(Humanoid) should return true.");

            var animator = AnimationBinder.Bind(character, RigMode.Humanoid);
            Assert.IsNotNull(animator, "AnimationBinder.Bind(Humanoid) returned null on SC-Body — " +
                             "SkeletonMap.SwitchRigMode(Humanoid) rejected the rig. If this fires, " +
                             "check SampleCharacterFactory.GenerateSCBody() bone naming.");
            Assert.IsNotNull(animator.avatar,
                "Humanoid Bind returned an Animator with no Avatar — BuildHumanoidAvatar's " +
                "output was null. The KHR plugin logs the rejection reason to the Console.");
            Assert.IsTrue(animator.avatar.isHuman,
                "Humanoid Bind produced an Avatar that isn't marked isHuman. Should never happen " +
                "given BuildHumanoidAvatar uses AvatarBuilder.BuildHumanAvatar.");
        }

        // ── Generic mode: works on any character, avatar deliberately null ──────────────────────

